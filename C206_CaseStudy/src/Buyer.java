
public class Buyer {
	
	private int id;
	private String name;
	private int mobileNum;
	private String email;
	
	public Buyer(int id, String name, int mobileNum, String email) {
		this.id = id;
		this.name = name;
		this.mobileNum = mobileNum;
		this.email = email;
	}

	public int getId() {
		return id;
	}

	public String getName() {
		return name;
	}

	public int getMobileNum() {
		return mobileNum;
	}

	public String getEmail() {
		return email;
	}
	
	
	
	

}
